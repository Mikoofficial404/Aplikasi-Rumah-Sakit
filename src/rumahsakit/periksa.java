    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggal = String.valueOf(fm.format(tgl.getDate()));

        try {
            Koneksi();
            String Kode = txtKode.getText();
            String KodeDokter = txtKodeDokter.getText();
            String NamaDokter = txtNamaDokter.getText();
            String KodePasien = txtKodePasien.getText();
            String NamaPasien = txtNamaPasien.getText();
            String Diagnosa = txtDiagnosa.getSelectedItem().toString();
            String Hasil = txtHasil.getSelectedItem().toString();

            int option = JOptionPane.showConfirmDialog(null, "Konfirmasi Update?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                String sql = "UPDATE periksa SET " +
                "Kd_Periksa='" + Kode + "', " +
                "Kd_Dokter='" + KodeDokter + "', " +
                "NamaDokter='" + NamaDokter + "', " +
                "Kd_Pasien='" + KodePasien + "', " +
                "NamaPasien='" + NamaPasien + "', " +
                "Diagnosa='" + Diagnosa + "', " +
                "tgl='" + tanggal + "', " +
                "Hasil='" + Hasil + "' " +
                "WHERE Kd_Periksa ='" + Kode + "'";
                cn.executeUpdate(sql);
                conn.close();
                tampilkan_data();
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Edit");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Edit");
            }
            tgl.setDateFormatString("");
        } catch (Exception e) {
            e.printStackTrace();
            System.exit(0);
        }
    }//GEN-LAST:event_EditActionPerformed

